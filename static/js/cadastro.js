// abre a sideNav 
function openNav() {
    document.getElementById("mySidenav").style.width = "250px";

    document.body.style.backgroundColor = B9DDE8;
}

// Fecha a sideNav
function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.body.style.backgroundColor = B9DDE8;
}